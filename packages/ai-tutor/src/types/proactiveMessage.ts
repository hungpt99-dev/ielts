// ─── Input types (for creating messages) ─────────────────────────────────────

export type ProactiveMessageInput = Omit<
  ProactiveMessage,
  'id' | 'createdAt' | 'isRead' | 'isDismissed' | 'isSnoozed' | 'autoGenerated'
>

export type ProactiveMessageUpdate = Partial<
  Pick<ProactiveMessage, 'isRead' | 'isDismissed' | 'isSnoozed' | 'snoozedUntil' | 'readAt' | 'dismissedAt' | 'interactionHistory' | 'feedback' | 'dismissReason' | 'actionClicked' | 'lastInteractedAt'>
>

export type ProactiveMessageInteractionResult = {
  message: ProactiveMessage | undefined
  success: boolean
  interaction: InteractionRecord
}

export interface InteractionStats {
  totalMessages: number
  totalDismissed: number
  totalSnoozed: number
  totalActionClicks: number
  totalFeedback: number
  helpfulCount: number
  notHelpfulCount: number
  dismissReasonCounts: Record<string, number>
  actionClickCounts: Record<string, number>
  interactionRate: number
}
