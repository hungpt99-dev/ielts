beforeEach(() => {
  localStorage.clear()
  resetRecentTriggers()
})

function setMaxMessages(n: number): void {
  const s = ProactiveMessageStorage.getDefaultSettings()
  s.maxMessagesPerDay = n
  ProactiveMessageStorage.saveSettings(s)
}

function setLearnerProfileData(profile: Record<string, unknown>): void {
  localStorage.setItem('ielts-learner-profile', JSON.stringify(profile))
}

function hasTriggerType(result: TriggerResult, triggerType: string): boolean {
  return result.messages.some(m => m.triggerType === triggerType)
}

function findTriggerType(result: TriggerResult, triggerType: string) {
  return result.messages.find(m => m.triggerType === triggerType)
}

// ─── onLessonCompleted ─────────────────────────────────────────────────────────

describe('onLessonCompleted', () => {
  it('generates a lesson_completed message', () => {
    const result = onLessonCompleted('IELTS Writing Task 2')
    expect(result.generated).toBeGreaterThanOrEqual(1)
    expect(hasTriggerType(result, 'lesson_completed')).toBe(true)
    const msg = findTriggerType(result, 'lesson_completed')!
    expect(msg.message).toContain('IELTS Writing Task 2')
  })

  it('emits the message via event bus', () => {
    const handler = vi.fn()
    const unsub = ProactiveEventBus.onNewMessage(handler)

    onLessonCompleted('Reading Practice 3')

    expect(handler).toHaveBeenCalled()
    const emittedTypes = handler.mock.calls.map((c: unknown[]) => (c[0] as { triggerType: string }).triggerType)
    expect(emittedTypes).toContain('lesson_completed')
    unsub()
  })

  it('returns empty result when throttled (simulate max messages reached)', () => {
    const today = new Date().toISOString()
    for (let i = 0; i < 5; i++) {
      ProactiveMessageStorage.addMessage({
        triggerType: 'due_review',
        category: 'vocabulary-review',
        title: `Existing ${i}`,
        message: 'Fill messages for today',
        priority: 'low',
        autoGenerated: true,
      })
    }
    const result = onLessonCompleted('Speaking Part 2')
    expect(result.generated).toBe(0)
  })
})

// ─── onRepeatedMistakes ────────────────────────────────────────────────────────

describe('onRepeatedMistakes', () => {
  it('generates a mistake_pattern_detected message for many mistakes', () => {
    const result = onRepeatedMistakes(8)
    expect(result.generated).toBeGreaterThanOrEqual(1)
    expect(hasTriggerType(result, 'mistake_pattern_detected')).toBe(true)
  })

  it('returns empty for few mistakes', () => {
    const result = onRepeatedMistakes(2)
    expect(hasTriggerType(result, 'mistake_pattern_detected')).toBe(false)
  })

  it('emits messages via event bus', () => {
    const handler = vi.fn()
    const unsub = ProactiveEventBus.onNewMessage(handler)

    onRepeatedMistakes(10)

    const mistakeMsgs = handler.mock.calls.filter(
      (c: unknown[]) => (c[0] as { triggerType: string }).triggerType === 'mistake_pattern_detected',
    )
    expect(mistakeMsgs.length).toBeGreaterThanOrEqual(1)
    unsub()
  })
})

