// ─── Trigger cooldown ──────────────────────────────────────────────────────────

describe('isTriggerOnCooldown', () => {
  it('returns false for unknown trigger name', () => {
    expect(isTriggerOnCooldown('unknown_trigger')).toBe(false)
  })

  it('returns false for a trigger that was never called', () => {
    expect(isTriggerOnCooldown('onLessonCompleted')).toBe(false)
  })

  it('returns true for a trigger recently called via canTrigger', () => {
    expect(canTrigger('onRepeatedMistakes')).toBe(true)
    expect(isTriggerOnCooldown('onRepeatedMistakes')).toBe(true)
  })

  it('returns false after reset', () => {
    canTrigger('onRepeatedMistakes')
    resetRecentTriggers()
    expect(isTriggerOnCooldown('onRepeatedMistakes')).toBe(false)
  })
})

// ─── canTrigger ────────────────────────────────────────────────────────────────

describe('canTrigger', () => {
  it('returns true when settings enabled and no cooldown', () => {
    const result = canTrigger('onLessonCompleted')
    expect(result).toBe(true)
  })

  it('returns false when called again within cooldown', () => {
    canTrigger('onLessonCompleted')
    expect(canTrigger('onLessonCompleted')).toBe(false)
  })

  it('allows different triggers independently', () => {
    canTrigger('onLessonCompleted')
    expect(canTrigger('onRepeatedMistakes')).toBe(true)
  })
})

// ─── Emitted messages are persisted ──────────────────────────────────────────

describe('message persistence', () => {
  it('messages from triggers are stored in ProactiveMessageStorage', () => {
    const before = ProactiveMessageStorage.loadMessages().length
    onLessonCompleted('Listening Test 1')
    const after = ProactiveMessageStorage.loadMessages().length
    expect(after).toBeGreaterThan(before)
  })

  it('storage contains the correct trigger type', () => {
    onMockTestReady()
    const stored = ProactiveMessageStorage.getMessagesByTriggerType('mock_test_ready')
    expect(stored.length).toBeGreaterThanOrEqual(1)
  })

  it('messages are retrievable after multiple triggers', () => {
    setMaxMessages(10)
    onLessonCompleted('Writing Task 1')
    onVocabularySaved(5)
    onRepeatedMistakes(10)
    const all = ProactiveMessageStorage.loadMessages()
    const types = new Set(all.map(m => m.triggerType))
    expect(types.has('lesson_completed')).toBe(true)
    expect(types.has('saved_word_exercise')).toBe(true)
    expect(types.has('mistake_pattern_detected')).toBe(true)
  })
})

// ─── No excessive/duplicate message generation ─────────────────────────────────

describe('duplicate prevention', () => {
  it('cooldown prevents duplicate triggers within time window', () => {
    const first = onLessonCompleted('Lesson 1')
    expect(first.generated).toBeGreaterThanOrEqual(1)

    const second = onLessonCompleted('Lesson 1 again')
    expect(second.generated).toBe(0)
  })

  it('different trigger types are not blocked by each other', () => {
    onLessonCompleted('Lesson 1')
    const vocabResult = onVocabularySaved(10)
    expect(vocabResult.generated).toBeGreaterThanOrEqual(1)
  })
})

