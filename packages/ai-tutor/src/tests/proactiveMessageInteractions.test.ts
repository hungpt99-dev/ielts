import { describe, it, expect, beforeEach } from 'vitest'
import {
  ProactiveMessageInteraction,
  DEFAULT_SNOOZE_DURATION_MS,
  SNOOZE_PRESETS,
  FEEDBACK_DISMISS_REASONS,
} from '../services/proactiveMessageInteraction'
import { ProactiveMessageStorage } from '../services/proactiveMessageStorage'
import { DEFAULT_PROACTIVE_SETTINGS } from '../types/proactiveMessage'
import type {
  ProactiveMessage,
  ProactiveMessageInput,
  ProactiveMessageSettings,
  InteractionRecord,
} from '../types/proactiveMessage'

beforeEach(() => {
  localStorage.clear()
})

function sampleInput(overrides: Partial<ProactiveMessageInput> = {}): ProactiveMessageInput {
  return {
    triggerType: 'due_review',
    category: 'vocabulary-review',
    title: 'Test Message',
    message: 'This is a test proactive message.',
    priority: 'medium',
    autoGenerated: false,
    ...overrides,
  }
}

function createSampleMessage(
  overrides: Partial<ProactiveMessageInput> = {},
): ProactiveMessage {
  return ProactiveMessageStorage.addMessage(sampleInput(overrides))
}

function createMessages(count: number): ProactiveMessage[] {
  const ids: string[] = []
  for (let i = 0; i < count; i++) {
    const msg = createSampleMessage({ title: `Message ${i}` })
    ids.push(msg.id)
  }
  return ProactiveMessageStorage.loadMessages()
}

// ─── dismissMessage ──────────────────────────────────────────────────────────

describe('ProactiveMessageInteraction — dismissMessage', () => {
  it('dismisses a message and sets isDismissed', () => {
    const msg = createSampleMessage()
    const result = ProactiveMessageInteraction.dismissMessage(msg.id)
    expect(result.success).toBe(true)
    expect(result.message).toBeDefined()
    expect(result.message!.isDismissed).toBe(true)
    expect(result.message!.dismissedAt).toBeTruthy()
    expect(result.interaction.type).toBe('dismiss')
  })

  it('records dismiss reason', () => {
    const msg = createSampleMessage()
    const result = ProactiveMessageInteraction.dismissMessage(msg.id, 'not-now')
    expect(result.success).toBe(true)
    expect(result.message!.dismissReason).toBe('not-now')
    expect(result.interaction.detail).toBe('not-now')
  })

  it('appends interaction to history', () => {
    const msg = createSampleMessage()
    ProactiveMessageInteraction.dismissMessage(msg.id, 'not-relevant')
    const loaded = ProactiveMessageStorage.getMessage(msg.id)
    expect(loaded!.interactionHistory).toHaveLength(1)
    expect(loaded!.interactionHistory![0].type).toBe('dismiss')
    expect(loaded!.lastInteractedAt).toBeTruthy()
  })

  it('returns success=false for nonexistent message', () => {
    const result = ProactiveMessageInteraction.dismissMessage('nonexistent')
    expect(result.success).toBe(false)
    expect(result.message).toBeUndefined()
  })

  it('passes metadata through to interaction record', () => {
    const msg = createSampleMessage()
    const result = ProactiveMessageInteraction.dismissMessage(msg.id, 'other', {
      source: 'dashboard',
      userId: 'user1',
    })
    expect(result.interaction.metadata).toEqual({
      source: 'dashboard',
      userId: 'user1',
    })
  })
})

// ─── snoozeMessage ───────────────────────────────────────────────────────────

describe('ProactiveMessageInteraction — snoozeMessage', () => {
  it('snoozes a message with default duration', () => {
    const msg = createSampleMessage()
    const result = ProactiveMessageInteraction.snoozeMessage(msg.id)
    expect(result.success).toBe(true)
    expect(result.message!.isSnoozed).toBe(true)
    expect(result.message!.snoozedUntil).toBeTruthy()
    expect(result.interaction.type).toBe('snooze')
    expect(result.interaction.detail).toBe('duration:3600000')
  })

  it('snoozes a message with custom duration', () => {
    const msg = createSampleMessage()
    const threeHours = 3 * 60 * 60 * 1000
    const result = ProactiveMessageInteraction.snoozeMessage(msg.id, threeHours)
    const expectedUntil = new Date(Date.now() + threeHours).getTime()
    const actualUntil = new Date(result.message!.snoozedUntil!).getTime()
    expect(Math.abs(actualUntil - expectedUntil)).toBeLessThan(100)
    expect(result.interaction.detail).toBe('duration:10800000')
  })

  it('appends interaction to history', () => {
    const msg = createSampleMessage()
    ProactiveMessageInteraction.snoozeMessage(msg.id)
    const loaded = ProactiveMessageStorage.getMessage(msg.id)
    expect(loaded!.interactionHistory).toHaveLength(1)
    expect(loaded!.interactionHistory![0].type).toBe('snooze')
    expect(loaded!.lastInteractedAt).toBeTruthy()
  })

  it('returns success=false for nonexistent message', () => {
    const result = ProactiveMessageInteraction.snoozeMessage('nonexistent')
    expect(result.success).toBe(false)
    expect(result.message).toBeUndefined()
  })
})

